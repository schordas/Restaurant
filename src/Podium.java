
public class Podium {
	int startX = 0;
	int startY = 0;
	int width = 0;
	int height = 0;
	
	public void setStartX(int startX){
		this.startX = startX;
	}
	public void setStartY(int startY){
		this.startY = startY;
	}
	public void setWidth(int width){
		this.width = width;
	}
	public void setHeight(int height){
		this.height = height;
	}
	
}
