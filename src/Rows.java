
public class Rows {
	int numRows = 0;
	int rowHeight= 0;
	String rowFontName = "";
	String rowFontType = "";
	int rowFontSize = 0;
	
	public void setNumRows(int numRows){
		this.numRows = numRows;
	}
	public void setRowHeight(int rowHeight){
		this.rowHeight = rowHeight;
	}
	public void setFontName(String rowFontName){
		this.rowFontName = rowFontName;
	}
	public void setFontType(String rowFontType){
		this.rowFontType = rowFontType;
	}
	public void setFontSize(int rowFontSize){
		this.rowFontSize = rowFontSize;
	}
}

