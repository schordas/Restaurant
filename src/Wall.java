
public class Wall {
	int xStrt = 0;
	int yStrt = 0;
	int xEnd = 0;
	int yEnd = 0;
	public  Wall(int xStrt, int yStrt, int xEnd, int yEnd){
		this.xStrt = xStrt;
		this.yStrt = yStrt;
		this.xEnd = xEnd;
		this.yEnd = yEnd;
	}
	public int getXStrt(){
		return this.xStrt;
	}
	
	public int getYStrt(){
		return this.yStrt;
	}
	
	public int getXEnd(){
		return this.xEnd;
	}
	
	public int getYEnd(){
		return this.yEnd;
	}
	
	
	
}
